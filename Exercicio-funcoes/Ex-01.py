# Escreva um programa em Python que solicita ao usuário um número inteiro e apresenta seu antecessor e sucessor.
 
numero = int(input('Digite um numero inteiro'))

antecessor = numero - 1
sucessor = numero + 1

print(f"O numero antecessor de: {numero} é {antecessor}")
print(f"O numero sucessor de: {numero} é {sucessor}")
